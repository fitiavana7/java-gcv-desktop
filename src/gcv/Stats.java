    private void showDataVoiture() throws Exception{
        try {
            ResultSet r = new gestion.GestionVersement().getDonneeByVoiture();
            int index = 0 ;
            pieChart2.clearData();
            while (r.next()) {      
                String voiture = r.getString(1);
                Integer montant = Integer.valueOf(r.getString(2)) ;
                pieChart2.addData(new ModelPieChart(voiture, montant, getColor(index ++)));
            }
        } catch (Exception e) {
        }
    }
