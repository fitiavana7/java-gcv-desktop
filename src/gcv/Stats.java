    private void showData() throws Exception{
        try {
            ResultSet r = new gestion.GestionVersement().getDonneeByChauffeur();
            int index = 0 ;
            pieChart1.clearData();
            while (r.next()) {      
                String nom = r.getString(1);
                Integer montant = Integer.valueOf(r.getString(2)) ;
                pieChart1.addData(new ModelPieChart(nom, montant, getColor(index ++)));
            }
        } catch (Exception e) {
        }
    }
    private Color getColor(int index){
        Color[] color = new Color[] {Color.BLUE , Color.GREEN , Color.YELLOW , Color.PINK , Color.RED , Color.CYAN} ;
        return color[index] ;
    }
