 private void chargerVoitures(){
        voituresC.removeAllItems();
        ArrayList<String> immatriculations = new ArrayList<String>();
        try{
                GestionVoiture gv = new GestionVoiture();
                ResultSet rs1 = gv.getAll();
                int i = 0 ;
                    while(rs1.next()){
                        immatriculations.add((String)rs1.getString("immatriculation"));
                        i++;
                    }
            }catch(Exception ex){
                ex.printStackTrace() ;
        }
        for(String item : immatriculations){
            voituresC.addItem(item);   
        }
   }
 
 public void ChargerListeFiltrer(){
            String titre[]={"id", "date" , "montant" , "ligne" , "point1" , "point2" , "voiture"} ;
            Object enreg[][]=new Object[1][1];
            try{
                GestionVersement gp = new GestionVersement();
                int nbr = gp.countenregByChauffeurAndDate(this.chaufeur_id , filtre.getText());
                enreg = new Object[nbr][titre.length] ;
                ResultSet rs1 = gp.getByChauffeurAndDate(this.chaufeur_id , filtre.getText());
                int i = 0 ;
                    while(rs1.next()){
                        enreg[i][0] = (Object)rs1.getString("id") ;
                        enreg[i][1] = (Object)rs1.getString("date") ;
                        enreg[i][2] = (Object)rs1.getString("montant");
                        enreg[i][3] = (Object)rs1.getString("ligne");
                        enreg[i][4] = (Object)rs1.getString("point1") ;
                        enreg[i][5] = (Object)rs1.getString("point2");
                        enreg[i][6] = (Object)rs1.getString("voiture");
                        i++;
                    }
            }catch(Exception ex){
                ex.printStackTrace() ;
            }
            jTable1.setModel(new DefaultTableModel(enreg, titre));
            jScrollPane2.setViewportView(jTable1);
    }
    
 
