    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        if (dateF.getText().length() < 4) {
            JOptionPane.showMessageDialog(null,"Date invalide (ex:22-05-2024)");
            dateF.requestFocus();
        } else {
            if ((int)montantF.getValue() < 2000) {
                JOptionPane.showMessageDialog(null,"Montant invalide (doit être plus de 2.000ar)");
            } else {
                if (ligneF.getText().length() < 0) {
                    JOptionPane.showMessageDialog(null,"Ligne invalide (ex: regional , national , 40 , 43 ,...)");
                    ligneF.requestFocus();
                } else {
                    if (point1F.getText().length() < 3 || point1F.getText().length() > 40) {
                        JOptionPane.showMessageDialog(null,"Point primus invalide (doit être 3-40 caractères)");
                        point1F.requestFocus();
                    } else {
                        if (point2F.getText().length() < 3 || point2F.getText().length() > 40) {
                            JOptionPane.showMessageDialog(null,"Point terminus invalide (doit être 3-40 caractères)");
                            point2F.requestFocus();
                        } else {
                            // TODO add your handling code here:
                            String date = dateF.getText();
                            int montant = (int) montantF.getValue();
                            String ligne = ligneF.getText();
                            String point1 = point1F.getText();
                            String point2 = point2F.getText();
                            String voiture = (String) voituresC.getSelectedItem();
                            try{
                                GestionVersement gv = new GestionVersement();
                                Versement v = new Versement(date,montant, ligne, point1, point2, this.chaufeur_id,0,voiture);
                                gv.insert(v);
                                ChargerListe();
                                Resetform();
                                JOptionPane.showMessageDialog(null,"Versement ajouté !");
                            }catch(Exception ex){
                                ex.printStackTrace() ;
                            }
                        }
                    }
                }
            }
        }    
    
    }//GEN-LAST:event_jButton6MouseClicked

